package study.beans;

public class QuizDto {
	
	private String quizContent;
	private int quizNo;
	
	public QuizDto() {
		super();
	}

	public String getQuizContent() {
		return quizContent;
	}

	public void setQuizContent(String quizContent) {
		this.quizContent = quizContent;
	}

	public int getQuizNo() {
		return quizNo;
	}

	public void setQuizNo(int quizNo) {
		this.quizNo = quizNo;
	}
	

}
