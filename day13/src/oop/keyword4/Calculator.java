package oop.keyword4;

//계산기 : 사칙연산 도우미
public class Calculator {

	//계산기능
	//static : 정적
	//- 객체 정보에 포함되지 않음
	//= 탈 객체지향 키워드
	//- 클래스에 고정시켜 편하게 사용하려는 목적 -> 객체 생성하지 않고 쓰겟당!
	public static int plus(int a, int b) {
		return a + b;
	}
	public static int minus(int a, int b) {
		return b - a;
	}
	
}
